<?php

namespace Nip\Utility\Traits;

/**
 * Trait SingletonTrait
 * @package Nip\Utility\Traits
 */
trait SingletonTrait
{

    /**
     * Singleton
     *
     * @return self
     */
    public static function instance()
    {
        static $instance;
        if (!($instance instanceof self)) {
            $instance = new self();
        }
        return $instance;
    }
}
